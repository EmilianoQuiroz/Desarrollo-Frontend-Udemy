//***** MODIFICAR UN ARREGLO *****//

const autos = ['BMW','Mercedes Benz','Mercedes','Audi'];

//Modificar elementos a un Array

autos[2] = 'Ford';/* Accedemos al indice que queremos modificar y agregamos el valor que queremos poner en ese indice */ 
 
console.log(autos);

//Agregar elementos a un Array

autos.push('Ferrari');/* Con .push podemos agregar un elemento nuevo al final de nuestro array */

console.log(autos);


